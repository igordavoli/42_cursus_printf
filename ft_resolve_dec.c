#include "ft_printf.h"

int	ft_resolve_dec(int n)
{
	int	i;

	ft_putnbr_fd(n, 1);
	if (n < 0)
	{
		i = 2;
		n *= -1;
	}
	else
		i = 1;
	n /= 10;
	while (n)
	{
		i++;
		n /= 10;
	}
	return (i);
}
