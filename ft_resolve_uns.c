#include "ft_printf.h"

int	ft_resolve_uns(unsigned int n)
{
	int	i;

	ft_putnbr(n);
	n /= 10;
	i = 1;
	while (n)
	{
		i++;
		n /= 10;
	}
	return (i);
}
